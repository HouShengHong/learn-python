# 對象的 bool
print('-----------------以下皆為False---------------------')

print(bool(False))
print(bool(0))
print(bool(0.0))
print(bool(None))
print(bool(''))
print(bool([]))
print(bool(list()))
print(bool(()))
print(bool(tuple()))
print(bool({}))
print(bool(dict()))
print(bool(set()))

print('-----------------其餘皆為True---------------------')
